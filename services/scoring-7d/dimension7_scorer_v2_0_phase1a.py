def score_by_percentile(value: float, bands: Dict, direction: str = 'higher_better') -> float:
    """
    Score a value using percentile bands.
    
    Args:
        value: Value to score
        bands: Scoring bands from percentiles
        direction: 'higher_better' or 'lower_better'
    
    Returns:
        Score (0-100)
    """
    if pd.isna(value):
        return 50.0  # Neutral score for missing data
    
    # Get band thresholds
    if direction == 'higher_better':
        if value >= bands['exceptional']['min']:
            # Exceptional: 90-100 (linear interpolation within band)
            return 95.0  # Simplified: mid-range of 90-100
        elif value >= bands['strong']['min']:
            # Strong: 75-89
            return 82.0
        elif value >= bands['moderate']['min']:
            # Moderate: 50-74
            return 62.0
        elif value >= bands['weak']['min']:
            # Weak: 25-49
            return 37.0
        else:
            # Poor: 0-24
            return 12.0
    
    elif direction == 'lower_better':
        if value <= bands['exceptional']['max']:
            return 95.0
        elif value <= bands['strong']['max']:
            return 82.0
        elif value <= bands['moderate']['max']:
            return 62.0
        elif value <= bands['weak']['max']:
            return 37.0
        else:
            return 12.0
    
    return 50.0  # Default neutral

def calculate_trading_activity(row: pd.Series, percentiles: Dict) -> Dict:
    """
    Component 1: Trading Activity (40% weight)
    
    Uses:
    - Trade Volume (50% of component)
    - Share Volume (50% of component)
    - Trade Density (informational - not scored directly)
    
    Args:
        row: Stock data row
        percentiles: Percentile thresholds
    
    Returns:
        Dictionary with component scores and details
    """
    # Get percentile bands
    tv_bands = percentiles['metrics']['trade_volume']['scoring_bands']
    sv_bands = percentiles['metrics']['share_volume']['scoring_bands']
    
    # Score trade volume
    trade_volume_score = score_by_percentile(
        row['trade_volume'], 
        tv_bands, 
        'higher_better'
    )
    
    # Score share volume
    share_volume_score = score_by_percentile(
        row['share_volume'], 
        sv_bands, 
        'higher_better'
    )
    
    # Combined activity score (50/50 weight)
    activity_score = (trade_volume_score * 0.5) + (share_volume_score * 0.5)
    
    # Trade density classification (informational)
    trade_density = row['trade_density']
    if pd.notna(trade_density):
        if trade_density >= 10000:
            density_class = 'institutional'
        elif trade_density >= 1000:
            density_class = 'mixed'
        else:
            density_class = 'retail'
    else:
        density_class = 'unknown'
    
    return {
        'component_score': activity_score,
        'trade_volume_score': trade_volume_score,
        'share_volume_score': share_volume_score,
        'trade_density_class': density_class
    }

def calculate_volatility_adjustment(row: pd.Series, percentiles: Dict) -> Dict:
    """
    Component 2: Volatility Adjustment (10% weight)
    
    Purpose: Fair comparison across volatility profiles
    - Very stable stocks (low vol): Slight bonus
    - Very volatile stocks (high vol): Slight penalty
    - Most stocks: Neutral
    
    Args:
        row: Stock data row
        percentiles: Percentile thresholds
    
    Returns:
        Dictionary with adjustment score
    """
    volatility = row['intraday_volatility']
    
    if pd.isna(volatility):
        return {'component_score': 50.0, 'volatility_level': 'unknown'}
    
    # Get percentile bands
    vol_bands = percentiles['metrics']['intraday_volatility']['scoring_bands']
    
    # Classification
    if volatility >= vol_bands['very_high']['min']:
        # Very volatile: 40 (penalty)
        return {'component_score': 40.0, 'volatility_level': 'very_high'}
    elif volatility >= vol_bands['high']['min']:
        # High volatility: 45 (slight penalty)
        return {'component_score': 45.0, 'volatility_level': 'high'}
    elif volatility <= vol_bands['very_low']['max']:
        # Very stable: 60 (bonus)
        return {'component_score': 60.0, 'volatility_level': 'very_low'}
    elif volatility <= vol_bands['low']['max']:
        # Stable: 55 (slight bonus)
        return {'component_score': 55.0, 'volatility_level': 'low'}
    else:
        # Moderate: 50 (neutral)
        return {'component_score': 50.0, 'volatility_level': 'moderate'}

def calculate_market_recognition(row: pd.Series) -> Dict:
    """
    Component 3: Market Recognition (50% weight)
    
    Uses v1.0 methodology (StockAnalysis.com data):
    - Analyst recommendations
    - Total return metrics
    
    Note: This component remains unchanged from v1.0 for Phase 1A
    Will be enhanced in future phases with more CSE data
    
    Args:
        row: Stock data row
    
    Returns:
        Dictionary with component score
    """
    # Placeholder: Use analyst_score if available
    # Otherwise neutral score
    
    if 'analyst_score' in row.index and pd.notna(row['analyst_score']):
        score = row['analyst_score']
    else:
        # Default neutral score if no analyst data
        score = 50.0
    
    return {
        'component_score': score,
        'data_source': 'v1.0_analyst_data'
    }

