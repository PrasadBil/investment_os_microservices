def load_percentiles(filepath: str = PERCENTILES_FILE) -> Dict:
    """
    Load CSE percentile thresholds.
    
    Args:
        filepath: Path to percentiles JSON
    
    Returns:
        Dictionary with percentile data
    """
    try:
        with open(filepath, 'r') as f:
            percentiles = json.load(f)
        
        print(f"✅ Loaded percentiles: {filepath}")
        print(f"   Generated: {percentiles['generated_at']}")
        print(f"   Stocks: {percentiles['stocks_analyzed']}")
        
        return percentiles
    
    except FileNotFoundError:
        print(f"❌ ERROR: Percentiles file not found: {filepath}")
        print("   Run: python3 cse_percentile_calculator.py cse_metrics.csv")
        sys.exit(1)
    except Exception as e:
        print(f"❌ ERROR loading percentiles: {e}")
        sys.exit(1)

