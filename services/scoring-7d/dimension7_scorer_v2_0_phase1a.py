def score_stocks(cse_metrics_file: str, output_file: str, report_file: Optional[str] = None):
    """
    Score all stocks using D7 v2.0 Phase 1A methodology.
    
    Args:
        cse_metrics_file: Input CSV with CSE metrics
        output_file: Output CSV for scores
        report_file: Optional output TXT for report
    """
    print("="*80)
    print("DIMENSION 7 SCORER V2.0 - PHASE 1A")
    print("="*80)
    
    # Load percentiles
    percentiles = load_percentiles()
    
    # Load CSE metrics
    print(f"\n📊 Loading CSE metrics: {cse_metrics_file}")
    df = pd.read_csv(cse_metrics_file)
    print(f"   Stocks: {len(df):,}")
    
    # Score each stock
    print("\n🔢 Calculating D7 v2.0 Phase 1A scores...")
    results = []
    
    for idx, row in df.iterrows():
        score_data = calculate_dimension7_v2(row, percentiles)
        results.append(score_data)
        
        if (idx + 1) % 50 == 0:
            print(f"   Processed {idx + 1}/{len(df)} stocks...")
    
    # Convert to DataFrame
    scores_df = pd.DataFrame(results)
    
    # Sort by score (descending)
    scores_df = scores_df.sort_values('dimension7_v2_score', ascending=False).reset_index(drop=True)
    
    # Add rank
    scores_df.insert(1, 'rank', range(1, len(scores_df) + 1))
    
    # Save scores
    scores_df.to_csv(output_file, index=False)
    print(f"\n💾 Scores saved: {output_file}")
    
    # Generate report
    if report_file:
        generate_report(scores_df, percentiles, report_file)
    
    # Print summary
    print_summary(scores_df)
    
    print("\n" + "="*80)
    print("✅ D7 V2.0 PHASE 1A SCORING COMPLETE")
    print("="*80)

