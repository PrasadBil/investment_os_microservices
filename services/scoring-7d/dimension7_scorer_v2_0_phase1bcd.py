def score_stocks(cse_metrics_file: str, output_file: str, report_file: Optional[str] = None):
    """
    Score all stocks using D7 v2.0 Phase 1B-D methodology.

    Args:
        cse_metrics_file: Input CSV with CSE metrics
        output_file: Output CSV for scores
        report_file: Optional output TXT for report
    """
    print("=" * 80)
    print("DIMENSION 7 SCORER V2.0 - PHASE 1B-D (FULL)")
    print("=" * 80)

    # Load percentiles
    percentiles = load_percentiles()

    # Load CSE metrics
    print(f"\n📊 Loading CSE metrics: {cse_metrics_file}")
    df = pd.read_csv(cse_metrics_file)
    print(f"   Stocks: {len(df):,}")
    print(f"   Columns: {', '.join(df.columns.tolist())}")

    # Verify required columns
    required_cols = ['symbol', 'trade_volume', 'share_volume', 'intraday_volatility']
    phase1bd_cols = ['volume_ratio_5d_20d', 'momentum_5d', 'momentum_20d', 'momentum_60d']

    missing_required = [c for c in required_cols if c not in df.columns]
    if missing_required:
        print(f"❌ ERROR: Missing required columns: {missing_required}")
        sys.exit(1)

    missing_new = [c for c in phase1bd_cols if c not in df.columns]
    if missing_new:
        print(f"⚠️  WARNING: Phase 1B-D columns missing: {missing_new}")
        print(f"   Components 4-5 will use neutral scores (50)")

    # Score each stock
    print(f"\n🔢 Calculating D7 v2.0 Phase 1B-D scores ({len(df)} stocks)...")
    print(f"   Weights: Activity={WEIGHTS['trading_activity']:.0%} | "
          f"Volatility={WEIGHTS['volatility_adjustment']:.0%} | "
          f"Recognition={WEIGHTS['market_recognition']:.0%} | "
          f"Volume={WEIGHTS['volume_trends']:.0%} | "
          f"Momentum={WEIGHTS['multi_timeframe_momentum']:.0%}")

    results = []

    for idx, row in df.iterrows():
        score_data = calculate_dimension7_v2(row, percentiles)
        results.append(score_data)

        if (idx + 1) % 50 == 0:
            print(f"   Processed {idx + 1}/{len(df)} stocks...")

    # Convert to DataFrame
    scores_df = pd.DataFrame(results)

    # Sort by score (descending)
    scores_df = scores_df.sort_values('dimension7_v2_score', ascending=False).reset_index(drop=True)

    # Add rank
    scores_df.insert(1, 'rank', range(1, len(scores_df) + 1))

    # Save scores
    scores_df.to_csv(output_file, index=False)
    print(f"\n💾 Scores saved: {output_file}")

    # Generate report
    if report_file:
        generate_report(scores_df, percentiles, report_file)

    # Print summary
    print_summary(scores_df)

    print("\n" + "=" * 80)
    print("✅ D7 V2.0 PHASE 1B-D SCORING COMPLETE")
    print("=" * 80)

