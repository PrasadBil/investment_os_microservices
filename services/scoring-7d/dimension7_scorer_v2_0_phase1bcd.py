def calculate_dimension7_v2(row: pd.Series, percentiles: Dict) -> Dict:
    """
    Calculate complete D7 v2.0 Phase 1B-D score.

    Components:
    1. Trading Activity (30%)
    2. Volatility Adjustment (10%)
    3. Market Recognition (25%)
    4. Volume Trends (10%)
    5. Multi-Timeframe Momentum (25%)

    Args:
        row: Stock data row
        percentiles: Percentile thresholds

    Returns:
        Dictionary with all scores and metadata
    """
    # Calculate all 5 components
    c1 = calculate_trading_activity(row, percentiles)
    c2 = calculate_volatility_adjustment(row, percentiles)
    c3 = calculate_market_recognition(row)
    c4 = calculate_volume_trends(row, percentiles)
    c5 = calculate_multi_timeframe_momentum(row, percentiles)

    # Weighted composite
    composite_score = (
        c1['component_score'] * WEIGHTS['trading_activity'] +
        c2['component_score'] * WEIGHTS['volatility_adjustment'] +
        c3['component_score'] * WEIGHTS['market_recognition'] +
        c4['component_score'] * WEIGHTS['volume_trends'] +
        c5['component_score'] * WEIGHTS['multi_timeframe_momentum']
    )

    # Round to 1 decimal
    composite_score = round(composite_score, 1)

    return {
        'symbol': row.get('symbol', 'UNKNOWN'),
        'dimension7_v2_score': composite_score,

        # Component scores
        'c1_trading_activity': round(c1['component_score'], 1),
        'c2_volatility_adj': round(c2['component_score'], 1),
        'c3_market_recognition': round(c3['component_score'], 1),
        'c4_volume_trends': round(c4['component_score'], 1),
        'c5_momentum': round(c5['component_score'], 1),

        # Detailed metrics
        'trade_volume_score': round(c1['trade_volume_score'], 1),
        'share_volume_score': round(c1['share_volume_score'], 1),
        'trade_density_class': c1['trade_density_class'],
        'volatility_level': c2['volatility_level'],
        'volume_trend': c4['volume_trend'],
        'volume_ratio': c4['volume_ratio'],
        'momentum_confluence': c5['confluence'],
        'momentum_5d_pct': c5['momentum_5d_pct'],
        'momentum_20d_pct': c5['momentum_20d_pct'],
        'momentum_60d_pct': c5['momentum_60d_pct'],
        'momentum_5d_score': round(c5['momentum_5d_score'], 1),
        'momentum_20d_score': round(c5['momentum_20d_score'], 1),
        'momentum_60d_score': round(c5['momentum_60d_score'], 1),

        # Metadata
        'phase': '1B-D',
        'components_active': 5,
        'metrics_available': 10,
        'scoring_date': datetime.now().strftime('%Y-%m-%d')
    }

