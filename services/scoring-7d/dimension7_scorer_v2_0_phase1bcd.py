def calculate_multi_timeframe_momentum(row: pd.Series, percentiles: Dict) -> Dict:
    """
    Component 5: Multi-Timeframe Momentum (25% weight)

    Uses three timeframes with sub-weights:
    - 5D return (30%): Recent catalysts, earnings reactions
    - 20D return (40%): Most actionable, filters noise
    - 60D return (30%): Sustained trends, avoids mean-reversion traps

    Multi-timeframe confluence logic:
    - All positive = Trend confirmation (highest conviction)
    - Mixed signals = Scored by sub-weighted average (natural dampening)
    - All negative = Sustained downtrend (lowest sentiment)

    Args:
        row: Stock data row
        percentiles: Percentile thresholds

    Returns:
        Dictionary with momentum scores and confluence assessment
    """
    momentum_5d = row.get('momentum_5d', np.nan)
    momentum_20d = row.get('momentum_20d', np.nan)
    momentum_60d = row.get('momentum_60d', np.nan)

    scores = {}
    timeframes = {
        '5d': momentum_5d,
        '20d': momentum_20d,
        '60d': momentum_60d
    }

    for tf, value in timeframes.items():
        metric_key = f'momentum_{tf}'
        if pd.isna(value):
            scores[tf] = 50.0  # Neutral for missing
        elif metric_key in percentiles['metrics']:
            bands = percentiles['metrics'][metric_key]['scoring_bands']
            scores[tf] = score_by_percentile(value, bands, 'higher_better')
        else:
            # Fallback: simple scoring if percentiles not yet generated
            if value >= 10.0:
                scores[tf] = 95.0
            elif value >= 3.0:
                scores[tf] = 82.0
            elif value >= 0.0:
                scores[tf] = 62.0
            elif value >= -5.0:
                scores[tf] = 37.0
            else:
                scores[tf] = 12.0

    # Sub-weighted composite
    momentum_score = (
        scores['5d'] * MOMENTUM_SUB_WEIGHTS['5d'] +
        scores['20d'] * MOMENTUM_SUB_WEIGHTS['20d'] +
        scores['60d'] * MOMENTUM_SUB_WEIGHTS['60d']
    )

    # Confluence assessment
    directions = []
    for tf, value in timeframes.items():
        if pd.notna(value):
            directions.append('positive' if value > 0 else 'negative' if value < 0 else 'flat')

    if all(d == 'positive' for d in directions):
        confluence = 'full_uptrend'
    elif all(d == 'negative' for d in directions):
        confluence = 'full_downtrend'
    elif len(directions) >= 2 and directions.count('positive') > directions.count('negative'):
        confluence = 'mostly_positive'
    elif len(directions) >= 2 and directions.count('negative') > directions.count('positive'):
        confluence = 'mostly_negative'
    else:
        confluence = 'mixed'

    return {
        'component_score': momentum_score,
        'momentum_5d_score': scores['5d'],
        'momentum_20d_score': scores['20d'],
        'momentum_60d_score': scores['60d'],
        'momentum_5d_pct': round(float(momentum_5d), 2) if pd.notna(momentum_5d) else None,
        'momentum_20d_pct': round(float(momentum_20d), 2) if pd.notna(momentum_20d) else None,
        'momentum_60d_pct': round(float(momentum_60d), 2) if pd.notna(momentum_60d) else None,
        'confluence': confluence
    }

