def calculate_market_recognition(row: pd.Series) -> Dict:
    """
    Component 3: Market Recognition (25% weight)

    Uses v1.0 methodology (StockAnalysis.com data):
    - Analyst recommendations and total return metrics
    - Falls back to neutral (50) if no analyst data available

    Note: Weight reduced from 50% (Phase 1A) to 25% (Phase 1B-D)
    because momentum data now provides a more direct market signal.

    Args:
        row: Stock data row

    Returns:
        Dictionary with component score
    """
    if 'analyst_score' in row.index and pd.notna(row.get('analyst_score')):
        score = row['analyst_score']
    else:
        score = 50.0

    return {
        'component_score': score,
        'data_source': 'v1.0_analyst_data'
    }

