# COMPONENT 4: VOLUME TRENDS (10%) — NEW
# =============================================================================

def calculate_volume_trends(row: pd.Series, percentiles: Dict) -> Dict:
    """
    Component 4: Volume Trends (10% weight)

    Purpose: Detect accumulation vs distribution using volume momentum.
    - 5D/20D volume ratio > 1: Recent volume above average (accumulation)
    - 5D/20D volume ratio < 1: Recent volume below average (distribution)

    Why it matters: Volume precedes price. Rising volume with flat price
    signals building pressure (institutional accumulation).

    Args:
        row: Stock data row
        percentiles: Percentile thresholds

    Returns:
        Dictionary with volume trend score and classification
    """
    volume_ratio = row.get('volume_ratio_5d_20d', np.nan)

    if pd.isna(volume_ratio):
        return {
            'component_score': 50.0,
            'volume_trend': 'unknown',
            'volume_ratio': None
        }

    # Check if we have percentile bands for this metric
    if 'volume_ratio_5d_20d' in percentiles['metrics']:
        vr_bands = percentiles['metrics']['volume_ratio_5d_20d']['scoring_bands']
        trend_score = score_by_percentile(volume_ratio, vr_bands, 'higher_better')
    else:
        # Fallback: hardcoded bands if percentiles not yet generated
        if volume_ratio >= 2.0:
            trend_score = 95.0
        elif volume_ratio >= 1.3:
            trend_score = 82.0
        elif volume_ratio >= 0.8:
            trend_score = 62.0
        elif volume_ratio >= 0.5:
            trend_score = 37.0
        else:
            trend_score = 12.0

    # Classify trend
    if volume_ratio >= 1.5:
        trend_class = 'strong_accumulation'
    elif volume_ratio >= 1.1:
        trend_class = 'mild_accumulation'
    elif volume_ratio >= 0.9:
        trend_class = 'neutral'
    elif volume_ratio >= 0.6:
        trend_class = 'mild_distribution'
    else:
        trend_class = 'strong_distribution'

    return {
        'component_score': trend_score,
        'volume_trend': trend_class,
        'volume_ratio': round(float(volume_ratio), 3)
    }

